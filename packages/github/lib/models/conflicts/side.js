/** @babel */

export default class Side {

  constructor(marker, source, position, banner, originalText) {
    this.marker = marker;
    this.source = source;
    this.position = position;
    this.banner = banner;
    this.originalText = originalText;
  }

  getMarker() {
    return this.marker;
  }

  getBannerMarker() {
    return this.banner.getMarker();
  }

}
